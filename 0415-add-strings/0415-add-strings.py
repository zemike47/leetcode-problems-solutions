class Solution(object):
    def addStrings(self, num1, num2):
        """
        :type num1: str
        :type num2: str
        :rtype: str
        """
        intNum1 = int(num1)
        intNum2 = int(num2)
        
        sum = intNum1 + intNum2
        
        sum = str(sum)
        
        return sum